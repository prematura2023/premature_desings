    <h3>
        Política de Privacidad de PREMATURE DESINGS

<br>

        Última actualización: 29/09/2023
        <br>
    
        En PREMATURE DESINGS, valoramos y respetamos tu privacidad. Esta Política de Privacidad describe cómo recopilamos, utilizamos y compartimos información personal cuando visitas nuestro sitio web www.premature/desings. Al utilizar nuestro sitio web, aceptas las prácticas descritas en esta política.
        
        Recopilación de Información Personal
        
        Recopilamos información personal que tú proporcionas voluntariamente cuando:
        <br>
        <br>

        Creas una cuenta en nuestro sitio web.
        Realizas una compra o completas una transacción.
        Te suscribes a nuestro boletín informativo o promociones.
        Participas en encuestas, concursos u otras actividades interactivas.
        Contactas a nuestro equipo de atención al cliente.
        La información personal que podemos recopilar incluye, pero no se limita a:
        
        Nombre
        Dirección de correo electrónico
        Dirección postal
        Número de teléfono
        Información de pago (como números de tarjeta de crédito o débito)
        Información demográfica
        Uso de la Información Personal
        
        Utilizamos la información personal que recopilamos para los siguientes propósitos:
        
        Procesar tus pedidos y entregarte los productos.
        Administrar tu cuenta y proporcionarte información relacionada con ella.
        Enviarte boletines informativos y promociones si te has suscrito.
        Personalizar tu experiencia en nuestro sitio web.
        Mejorar nuestros productos y servicios.
        Realizar investigaciones de mercado y análisis de datos.
        Responder a tus consultas y solicitudes de atención al cliente.
        Compartir Información Personal
        <br>
        <br>
        
        No vendemos ni alquilamos tu información personal a terceros. Sin embargo, podemos compartir tu información con terceros en las siguientes situaciones:
        
        Con proveedores de servicios que nos ayudan a procesar pedidos, enviar correos electrónicos, analizar datos y brindar servicios técnicos y de atención al cliente.
        Cuando sea necesario para cumplir con requisitos legales, regulaciones gubernamentales, procesos judiciales o solicitudes de autoridades competentes.
        Para proteger nuestros derechos, privacidad, seguridad o propiedad, o los de nuestros usuarios y otros.
        En caso de una fusión, adquisición, reorganización o venta de activos, tu información personal podría transferirse a una entidad sucesora.
        Seguridad de la Información
        
        Tomamos medidas razonables para proteger tu información personal contra accesos no autorizados, divulgación, alteración o destrucción. Sin embargo, ninguna transmisión de datos por Internet o almacenamiento electrónico es completamente segura, y no podemos garantizar la seguridad absoluta de tus datos.
        
        Tus Derechos de Privacidad
        
        Puedes ejercer tus derechos de privacidad, como acceder, corregir, eliminar o limitar el uso de tu información personal, enviando un correo electrónico a juanjosemira12@gmail.com. También tienes derecho a darte de baja de nuestros boletines informativos en cualquier momento.
        
        Cambios en esta Política de Privacidad
        
        Nos reservamos el derecho de actualizar esta Política de Privacidad en cualquier momento. Te notificaremos sobre cambios importantes a través de un aviso en nuestro sitio web o por otros medios.
        
        Contacto
        
